<?php
// You can use define() Function to put any value in a Key-Word, which is known as DEFINED KEYWORD or CONSTANT.
// This is something like Variable but not exactly Variable. Because you can put different values in different times in same Variable.
// But you can not change the value of your DEFINED KEYWORD.

	define("GREETING", "Welcome to our Training Centre..."); // You have Defined your Keyword GREETINGS
	
	echo GREETING; // When you are giving echo of the DEFINED KEYWORD, it is showing us the Message that you have put inside your DEFINED KEYWORD.
	
?>